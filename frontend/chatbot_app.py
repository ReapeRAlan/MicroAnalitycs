    def detect_prediction_intent(self, message: str) -> bool:
        """Detectar si el usuario quiere hacer una predicci√≥n"""
        prediction_keywords = [
            'predic', 'demanda', 'pron√≥stico', 'forecast', 'estimar', 
            'proyectar', 'cu√°nto vender', 'futuro', 'pr√≥ximos d√≠as',
            'ventas futuras', 'qu√© esperar', 'modelo', 'machine learning',
            'ml', 'algoritmo', 'comparar modelo', 'mejor modelo'
        ]
        
        message_lower = message.lower()
        return any(keyword in message_lower for keyword in prediction_keywords)
    
    def detect_model_comparison_intent(self, message: str) -> bool:
        """Detectar si el usuario quiere comparar modelos"""
        comparison_keywords = [
            'comparar modelo', 'mejor modelo', 'cu√°l modelo', 'qu√© modelo',
            'evaluar modelo', 'modelo m√°s preciso', 'comparaci√≥n', 'algoritmo',
            'machine learning', 'ml', 'linear', 'polynomial', 'precisi√≥n'
        ]
        
        message_lower = message.lower()
        return any(keyword in message_lower for keyword in comparison_keywords)
    
    def detect_needs_product_selection(self, message: str) -> bool:
        """Detectar si el mensaje necesita selecci√≥n de producto"""
        # Si ya menciona un ID espec√≠fico, no necesita selector
        import re
        if re.search(r'producto\s+\d+', message.lower()):
            return False
        
        needs_product_keywords = [
            'predic', 'inventario de producto', 'stock de producto',
            'ventas de producto', 'an√°lisis producto', 'modelo para producto',
            'comparar modelo', 'mejor modelo', 'tendencia producto'
        ]
        
        message_lower = message.lower()
        return any(keyword in message_lower for keyword in needs_product_keywords)
    
    def suggest_product_selection(self, message: str) -> str:
        """Sugerir selecci√≥n de producto cuando sea necesario"""
        if self.detect_needs_product_selection(message):
            return ("üéØ **Tu consulta necesita un producto espec√≠fico**\n\n"
                   "Para darte una respuesta precisa, necesito saber sobre qu√© producto quieres informaci√≥n. "
                   "Puedes:\n\n"
                   "1Ô∏è‚É£ Usar el **Selector Interactivo** (aparece abajo)\n"
                   "2Ô∏è‚É£ Especificar el ID: `predecir producto 1`\n"
                   "3Ô∏è‚É£ Mencionar el nombre: `an√°lisis de Laptop Gaming`\n\n"
                   "üí° **Ejemplo:** `comparar modelos para producto 2`")
        return ""

    def check_backend_connection(self) -> bool:
        """Verificar conexi√≥n con el backend"""
        try:
            response = requests.get(f"{self.backend_url}/api/chatbot/health", timeout=3)
            return response.status_code == 200
        except Exception:
            return False
    
    def send_message_to_backend(self, message: str) -> Dict[str, Any]:
        """Enviar mensaje al backend"""
        try:
            payload = {
                "content": message,
                "session_id": self.session_id,
                "timestamp": datetime.now().isoformat()
            }
            
            response = requests.post(
                f"{self.backend_url}/api/chatbot/message",
                json=payload,
                timeout=10
            )
            
            if response.status_code == 200:
                return response.json()
            else:
                return {
                    "success": False,
                    "response": f"Error del servidor: {response.status_code}",
                    "fallback": True
                }
                
        except requests.RequestException as e:
            return {
                "success": False,
                "response": self._get_fallback_response(message),
                "fallback": True,
                "error": str(e)
            }
    
